def print_prediction_validation(df: pd.DataFrame, slice_type: str):
    """Validate predictions against expected behavior"""
    print(f"\n{'─'*90}")
    print("4. PREDICTION VALIDATION")
    print(f"{'─'*90}")

    # Define thresholds for "low activity" features
    LOW_THRESHOLDS = {
        'RRU.PrbTotDl': 20,
        'DRB.UEThpDl': 10,
        'DRB.RlcSduDelayDl': 50,  # 50ms
        'DRB.PdcpSduVolumeDL': 100,
        'DRB.PdcpSduVolumeUL': 100
    }

    # Find rows with low activity but predicted as Heavy/Medium
    low_activity_mask = (
        (df['RRU.PrbTotDl'] < LOW_THRESHOLDS['RRU.PrbTotDl']) &
        (df['DRB.UEThpDl'] < LOW_THRESHOLDS['DRB.UEThpDl']) &
        (df['DRB.PdcpSduVolumeDL'] < LOW_THRESHOLDS['DRB.PdcpSduVolumeDL']) &
        (df['DRB.PdcpSduVolumeUL'] < LOW_THRESHOLDS['DRB.PdcpSduVolumeUL'])
    )

    suspicious_heavy = df[low_activity_mask & (df['pred_load_label'] == 'Heavy')]
    suspicious_medium = df[low_activity_mask & (df['pred_load_label'] == 'Medium')]

    print(f"\n🔍 Low Activity Analysis:")
    print(f"  Criteria: PRB<{LOW_THRESHOLDS['RRU.PrbTotDl']}, "
          f"Tput<{LOW_THRESHOLDS['DRB.UEThpDl']}, "
          f"Vol<{LOW_THRESHOLDS['DRB.PdcpSduVolumeDL']}")
    print(f"\n  Total low activity rows:  {low_activity_mask.sum()} ({low_activity_mask.sum()/len(df):.1%})")
    print(f"  ⚠️  Predicted as Heavy:    {len(suspicious_heavy)} ({len(suspicious_heavy)/len(df):.1%})")
    print(f"  ⚠️  Predicted as Medium:   {len(suspicious_medium)} ({len(suspicious_medium)/len(df):.1%})")
    print(f"  ✓  Predicted as Light:    {low_activity_mask.sum() - len(suspicious_heavy) - len(suspicious_medium)}")

    if len(suspicious_heavy) > 0 or len(suspicious_medium) > 0:
        print(f"\n  💡 Insight: {len(suspicious_heavy) + len(suspicious_medium)} potentially incorrect predictions")
        print(f"     These rows have very low features but are predicted as Heavy/Medium.")

    # Expected load distribution by slice type
    expected_distribution = {
        'eMBB': {'Light': (0, 20), 'Medium': (30, 50), 'Heavy': (40, 70)},
        'mMTC': {'Light': (40, 70), 'Medium': (20, 40), 'Heavy': (0, 20)},
        'URLLC': {'Light': (30, 60), 'Medium': (20, 40), 'Heavy': (10, 30)}
    }

    print(f"\n🎯 Expected Distribution for {slice_type}:")
    actual_dist = df['pred_load_label'].value_counts(normalize=True) * 100

    if slice_type in expected_distribution:
        for label, (min_exp, max_exp) in expected_distribution[slice_type].items():
            actual = actual_dist.get(label, 0)
            status = "✓" if min_exp <= actual <= max_exp else "⚠️"
            print(f"  {status} {label:<8}: Expected {min_exp}-{max_exp}%, Actual {actual:.1f}%")


def print_anomalies(df: pd.DataFrame):
    """Detect and report anomalies"""
    print(f"\n{'─'*90}")
    print("5. ANOMALY DETECTION")
    print(f"{'─'*90}")

    anomalies = []

    # Anomaly 1: Zero throughput but Heavy load
    zero_tput_heavy = df[(df['DRB.UEThpDl'] == 0) & (df['pred_load_label'] == 'Heavy')]
    if len(zero_tput_heavy) > 0:
        anomalies.append(f"⚠️  {len(zero_tput_heavy)} rows: Zero throughput but predicted Heavy")

    # Anomaly 2: Very high PRB but Light load
    high_prb_light = df[(df['RRU.PrbTotDl'] > 80) & (df['pred_load_label'] == 'Light')]
    if len(high_prb_light) > 0:
        anomalies.append(f"⚠️  {len(high_prb_light)} rows: High PRB (>80%) but predicted Light")

    # Anomaly 3: High throughput but low allocation
    high_tput_low_alloc = df[(df['DRB.UEThpDl'] > 100) & (df['pred_action'] == 0)]
    if len(high_tput_low_alloc) > 0:
        anomalies.append(f"⚠️  {len(high_tput_low_alloc)} rows: High throughput (>100 Mbps) but low allocation (20%)")

    # Anomaly 4: Very high delay but Light load
    high_delay_light = df[(df['DRB.RlcSduDelayDl'] > 500) & (df['pred_load_label'] == 'Light')]
    if len(high_delay_light) > 0:
        anomalies.append(f"⚠️  {len(high_delay_light)} rows: High delay (>500ms) but predicted Light")

    # Anomaly 5: Constant predictions (model might be stuck)
    if df['pred_load_label'].nunique() == 1:
        anomalies.append(f"⚠️  All predictions are '{df['pred_load_label'].iloc[0]}' - model may need retraining")

    if anomalies:
        print(f"\n🚨 Detected Anomalies:")
        for i, anomaly in enumerate(anomalies, 1):
            print(f"  {i}. {anomaly}")
    else:
        print(f"\n✓ No significant anomalies detected")


def print_action_load_consistency(df: pd.DataFrame):
    """Check consistency between load predictions and action decisions"""
    print(f"\n{'─'*90}")
    print("6. ACTION-LOAD CONSISTENCY")
    print(f"{'─'*90}")

    print(f"\n📊 Action Distribution by Load Label:")
    print(f"{'Load':<10} {'Action 0 (20%)':>16} {'Action 1 (50%)':>16} {'Action 2 (80%)':>16}")
    print(f"{'-'*60}")

    for load in ['Light', 'Medium', 'Heavy']:
        load_data = df[df['pred_load_label'] == load]
        if len(load_data) > 0:
            action_counts = load_data['pred_action'].value_counts()
            a0 = action_counts.get(0, 0) / len(load_data) * 100
            a1 = action_counts.get(1, 0) / len(load_data) * 100
            a2 = action_counts.get(2, 0) / len(load_data) * 100
            print(f"{load:<10} {a0:>15.1f}% {a1:>15.1f}% {a2:>15.1f}%")

    # Check for inconsistencies
    print(f"\n💡 Consistency Insights:")

    # Heavy load should prefer high allocation
    heavy_data = df[df['pred_load_label'] == 'Heavy']
    if len(heavy_data) > 0:
        heavy_low_alloc = (heavy_data['pred_action'] == 0).sum()
        if heavy_low_alloc / len(heavy_data) > 0.5:
            print(f"  ⚠️  {heavy_low_alloc}/{len(heavy_data)} Heavy loads got low allocation (20%)")
        else:
            print(f"  ✓  Heavy loads appropriately allocated")

    # Light load should prefer low allocation
    light_data = df[df['pred_load_label'] == 'Light']
    if len(light_data) > 0:
        light_high_alloc = (light_data['pred_action'] == 2).sum()
        if light_high_alloc / len(light_data) > 0.3:
            print(f"  ⚠️  {light_high_alloc}/{len(light_data)} Light loads got high allocation (80%)")
        else:
            print(f"  ✓  Light loads appropriately allocated")


