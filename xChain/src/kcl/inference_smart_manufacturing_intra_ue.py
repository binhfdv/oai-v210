def add_inference_results(df, engine, verbose=False):
    """
    Run inference on every 5 rows and add results as new columns.

    Parameters
    ----------
    df : pd.DataFrame
        Single UE dataframe (e.g., embb_ue3)
    engine : ORANInferenceEngine
        Inference engine with predict method
    verbose : bool
        Whether to show inference progress

    Returns
    -------
    df_with_results : pd.DataFrame
        DataFrame with added columns: pred_load_label, pred_action, pred_allocation
    """
    # Initialize lists to store results
    load_labels_list = []
    actions_list = []
    allocations_list = []

    # Process in batches of 5 rows
    num_batches = len(df) // 5

    for i in range(num_batches):
        start_idx = i * 5
        end_idx = start_idx + 5
        batch = df.iloc[start_idx:end_idx]

        # Run inference
        results = engine.predict(batch, verbose=verbose)

        # Extract and store results
        load_labels_list.extend(results['load_labels'])
        actions_list.extend(results['actions'])
        allocations_list.extend(results['allocations'])

    # Handle remaining rows if not divisible by 5
    remaining_rows = len(df) % 5
    if remaining_rows > 0:
        print(f"Warning: {remaining_rows} rows at the end were not processed (not a full batch of 5)")

    # Create result dataframe (only processed rows)
    df_with_results = df.iloc[:num_batches * 5].copy()
    df_with_results['pred_load_label'] = load_labels_list
    df_with_results['pred_action'] = actions_list
    df_with_results['pred_allocation'] = allocations_list

    return df_with_results

