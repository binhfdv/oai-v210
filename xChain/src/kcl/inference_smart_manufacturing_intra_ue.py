"""## Inference Engine"""

class ORANInferenceEngine:
    def __init__(self):
        self.labeling_xapp = None
        self.resource_xapp = None
        self.preprocessor = KPMPreprocessor()
        self.action_map = {0: 0.2, 1: 0.5, 2: 0.8}
        self.load_labels = ['Light', 'Medium', 'Heavy']
        self.device = device

    def load_models(self, labeling_path: str, resource_path: str):
        print(f"Loading models...")

        # Load LabelingxApp
        self.labeling_xapp = LabelingxApp(in_dim=5).to(self.device)
        checkpoint = torch.load(labeling_path, map_location=self.device)
        if isinstance(checkpoint, dict) and 'model_state_dict' in checkpoint:
            self.labeling_xapp.load_state_dict(checkpoint['model_state_dict'])
        else:
            self.labeling_xapp.load_state_dict(checkpoint)
        self.labeling_xapp.eval()
        print(f"  ✓ LabelingxApp loaded")

        # Load ResourcexApp
        self.resource_xapp = ResourcexApp()
        checkpoint = torch.load(resource_path, map_location=self.device)
        if isinstance(checkpoint, dict) and 'model_state_dict' in checkpoint:
            self.resource_xapp.q_net.load_state_dict(checkpoint['model_state_dict'])
        else:
            self.resource_xapp.q_net.load_state_dict(checkpoint)
        self.resource_xapp.q_net.eval()
        print(f"  ✓ ResourcexApp loaded (state_dim=8)")

    def fit_preprocessor(self, historical_data: Union[pd.DataFrame, str]):
        self.preprocessor.fit(historical_data)

    def predict(self, data: Union[pd.DataFrame, np.ndarray, dict], verbose: bool = True) -> Dict:
        if self.labeling_xapp is None or self.resource_xapp is None:
            raise ValueError("Models not loaded! Call load_models() first.")

        graph = self.preprocessor.transform_to_graph(data)
        num_ues = len(graph.x)

        # Stage 1: LabelingxApp (GNN classification)
        with torch.no_grad():
            logits = self.labeling_xapp(graph.x, graph.edge_index)
            load_predictions = logits.argmax(dim=1).cpu().numpy()  # Class indices (0, 1, 2)
            embeddings = self.labeling_xapp.embeddings.cpu().numpy()  # Shape: (num_ues, 32)

        # Extract metrics from graph
        prb = graph.prb
        tput = graph.tput
        delay = graph.delay

        # Normalize metrics (MATCHING TRAINING CODE)
        prb_norm = prb / 100.0
        tput_norm = tput / 20.0
        delay_norm = delay / 0.01
        emb_dim = embeddings[:, 0]  # CRITICAL: Only use first embedding dimension!

        # Construct RL states (EXACTLY AS IN TRAINING)
        # State: [load_pred, prb_norm, tput_norm, delay_norm, emb_dim, prb*0.1, tput*0.1, delay*0.1]
        states = np.column_stack([
            load_predictions,   # 1D: class index (0, 1, or 2)
            prb_norm,           # 1D: normalized PRB
            tput_norm,          # 1D: normalized throughput
            delay_norm,         # 1D: normalized delay
            emb_dim,            # 1D: first embedding dimension
            prb_norm * 0.1,     # 1D: scaled PRB
            tput_norm * 0.1,    # 1D: scaled throughput
            delay_norm * 0.1    # 1D: scaled delay
        ])  # Shape: (num_ues, 8)

        if verbose:
            print(f"DEBUG: States shape = {states.shape}")  # Should be (num_ues, 8)
            print(f"DEBUG: First state = {states[0]}")

        # Stage 2: ResourcexApp (RL allocation)
        actions = []
        for i in range(num_ues):
            action = self.resource_xapp.act(states[i], epsilon=0.0)
            actions.append(action)

        # Convert actions to allocations
        allocations = [self.action_map[a] for a in actions]
        total_alloc = sum(allocations)
        if total_alloc > 1.0:
            allocations = [a / total_alloc for a in allocations]

        results = {
            'load_labels': [self.load_labels[p] for p in load_predictions],
            'load_predictions': load_predictions,
            'actions': actions,
            'allocations': allocations,
            'embeddings': embeddings,
            'states': states,
            'prb': prb,
            'tput': tput,
            'delay': delay,
            'num_ues': num_ues
        }

        if verbose:
            self._print_results(results)

        return results

    def _print_results(self, results: Dict):
      print("\n" + "="*90)
      print("O-RAN DUAL xAPPS INFERENCE RESULTS")
      print("="*90)
      print(f"Number of UEs: {results['num_ues']}")
      print("-"*90)
      print(f"{'UE':<4} {'Load':<8} {'Action':<8} {'Allocation':<12} {'PRB':<10} {'Thput':<12} {'Delay':<12}")
      print("-"*90)

      for i in range(results['num_ues']):
          print(f"{i:<4} "
                f"{results['load_labels'][i]:<8} "
                f"{results['actions'][i]:<8} "
                f"{results['allocations'][i]:<12.1%} "
                f"{results['prb'][i]:<10.1f} "
                f"{results['tput'][i]:<12.2f} "
                f"{results['delay'][i]:<12.4f}")

      print("-"*90)
      print(f"Total Allocation: {sum(results['allocations']):.1%}")
      print("="*90 + "\n")

print("✓ Inference engine defined")

