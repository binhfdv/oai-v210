def split_by_ue(df: pd.DataFrame, slice_name: str) -> Dict[int, pd.DataFrame]:
    """
    Split KPM data by UE based on predefined ranges

    Parameters
    ----------
    df : pd.DataFrame
        Input KPM dataframe
    slice_name : str
        'embb', 'mmtc', or 'urllc'

    Returns
    -------
    ue_data : dict
        Dictionary mapping UE_ID → DataFrame

    Example
    -------
    >>> embb = pd.read_csv("oai-kpm-2026-02-23-085959-embb.csv")
    >>> ue_data = split_by_ue(embb, 'embb')
    >>> ue3_data = ue_data[3]  # Data for UE 3
    """
    if slice_name not in UE_RANGES:
        raise ValueError(f"Unknown slice: {slice_name}. Must be 'embb', 'mmtc', or 'urllc'")

    ue_data = {}
    ranges = UE_RANGES[slice_name]

    print(f"\nSplitting {slice_name.upper()} by UE:")
    for ue_id, (start, end) in ranges.items():
        if end is None:
            ue_df = df.iloc[start:].copy().reset_index(drop=True)
        else:
            ue_df = df.iloc[start:end].copy().reset_index(drop=True)

        ue_data[ue_id] = ue_df
        print(f"  UE {ue_id}: rows {start:5d}-{end if end else 'end':>5} → {len(ue_df):5d} samples")

    return ue_data


def load_and_split(embb_path: str, mmtc_path: str, urllc_path: str) -> Dict[str, Dict[int, pd.DataFrame]]:
    """
    Load all KPM files and split by UE

    Parameters
    ----------
    embb_path : str
        Path to eMBB KPM CSV
    mmtc_path : str
        Path to mMTC KPM CSV
    urllc_path : str
        Path to URLLC KPM CSV

    Returns
    -------
    all_data : dict
        Nested dictionary: slice_name → {UE_ID → DataFrame}

    Example
    -------
    >>> data = load_and_split('embb.csv', 'mmtc.csv', 'urllc.csv')
    >>> embb_ue3 = data['embb'][3]
    >>> mmtc_ue1 = data['mmtc'][1]
    >>> urllc_ue2 = data['urllc'][2]
    """
    print("Loading KPM files...")

    # Load data
    embb = pd.read_csv(embb_path)
    mmtc = pd.read_csv(mmtc_path)
    urllc = pd.read_csv(urllc_path)

    print(f"  ✓ eMBB:  {len(embb):6d} rows")
    print(f"  ✓ mMTC:  {len(mmtc):6d} rows")
    print(f"  ✓ URLLC: {len(urllc):6d} rows")

    # Split by UE
    all_data = {
        'embb': split_by_ue(embb, 'embb'),
        'mmtc': split_by_ue(mmtc, 'mmtc'),
        'urllc': split_by_ue(urllc, 'urllc')
    }

    return all_data


