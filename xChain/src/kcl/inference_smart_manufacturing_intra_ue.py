"""## Data Preprocessor"""

class KPMPreprocessor:
    def __init__(self):
        self.scaler = RobustScaler()
        self.fitted = False
        self.feature_cols = [
            'RRU.PrbTotDl',
            'DRB.UEThpDl',
            'DRB.RlcSduDelayDl',
            'DRB.PdcpSduVolumeDL',
            'DRB.PdcpSduVolumeUL'
        ]

    def fit(self, data: Union[pd.DataFrame, str]):
        if isinstance(data, str):
            df = pd.read_csv(data)
        else:
            df = data

        features = df[self.feature_cols].fillna(method='ffill').fillna(0).values
        self.scaler.fit(features)
        self.fitted = True
        print(f"✓ Scaler fitted on {len(df)} samples")

    def transform_to_graph(self, data: Union[pd.DataFrame, np.ndarray, dict]) -> Data:
        if not self.fitted:
            raise ValueError("Scaler not fitted! Call fit() first.")

        if isinstance(data, dict):
            features = data['features']
            prb = data.get('prb', np.full(len(features), 30.0))
            tput = data.get('tput', np.full(len(features), 5.0))
            delay = data.get('delay', np.full(len(features), 0.003))
        elif isinstance(data, np.ndarray):
            if data.shape[1] != 5:
                raise ValueError(f"Expected 5 features, got {data.shape[1]}")
            features = data
            prb = features[:, 0]
            tput = features[:, 1]
            delay = features[:, 2]
        elif isinstance(data, pd.DataFrame):
            features = data[self.feature_cols].fillna(method='ffill').fillna(0).values
            prb = data['RRU.PrbTotDl'].fillna(30).values
            tput = data['DRB.UEThpDl'].fillna(5).values
            delay = data['DRB.RlcSduDelayDl'].fillna(0.003).values
        else:
            raise ValueError(f"Unsupported data type: {type(data)}")

        features_scaled = self.scaler.transform(features).astype(np.float32)

        n = len(features_scaled)
        edges = []
        for i in range(n):
            edges.append([i, (i + 1) % n])
            edges.append([i, (i + n - 1) % n])

        graph = Data(
            x=torch.FloatTensor(features_scaled),
            edge_index=torch.LongTensor(np.array(edges).T),
            prb=prb,
            tput=tput,
            delay=delay
        )

        return graph

