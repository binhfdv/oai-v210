def generate_summary_report(embb_results: Dict[int, pd.DataFrame],
                           mmtc_results: Dict[int, pd.DataFrame],
                           urllc_results: Dict[int, pd.DataFrame]):
    """
    Generate high-level summary across all slices
    """
    print("\n" + "="*90)
    print("SUMMARY REPORT: ALL SLICES")
    print("="*90)

    slices = {
        'eMBB': embb_results,
        'mMTC': mmtc_results,
        'URLLC': urllc_results
    }

    print(f"\n{'Slice':<10} {'UEs':>6} {'Samples':>10} {'Light':>10} {'Medium':>10} {'Heavy':>10}")
    print(f"{'-'*58}")

    for slice_name, ue_results in slices.items():
        if not ue_results:
            continue

        # Combine all UEs for this slice
        all_data = pd.concat(ue_results.values(), ignore_index=True)

        num_ues = len(ue_results)
        num_samples = len(all_data)

        load_dist = all_data['pred_load_label'].value_counts(normalize=True) * 100
        light = load_dist.get('Light', 0)
        medium = load_dist.get('Medium', 0)
        heavy = load_dist.get('Heavy', 0)

        print(f"{slice_name:<10} {num_ues:>6} {num_samples:>10} {light:>9.1f}% {medium:>9.1f}% {heavy:>9.1f}%")

    print("="*90 + "\n")


